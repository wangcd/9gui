         <font class="f5 f6" style="font-size:18px; ">红酒课堂</font><br /><font class="f3">玖櫃 / 2014-05-29</font>
         </div>
                   <p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">真要用一句话来概括勃艮第葡萄酒的特性的话，我想没有比123456更能概括勃艮第的特点了：即1种土质，2个品种，3种产酒单位，4级葡萄园，5大产区&hellip;...&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">一种土质：&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">虽然勃艮第葡萄园里的土质变化多端，但主要还是由位于山坡上的石灰质黏土所构成，只是来自不同年代的沉积，以及黏土，石灰与砂质石块的比例有所同罢了。 永远追求优雅、对土地的忠诚，只有读懂勃艮第酿酒者的这份对土地品质的执着和对自然的追求，才能真正的理解勃艮第葡萄酒的优雅和高贵。勃艮第人相信自己在自己的土地上酿出来的葡萄酒是最好的，更不会轻易去迎合那些美国酒评家的口味。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">两种葡萄品种：&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">勃艮第接近寒冷的大陆性气候区，适合采用单一品种酿造葡萄酒。除了少数例外，勃艮第的红酒都是采用黑比诺(Pinot Noir)葡萄酿成,白酒则完全是霞多丽（chardonnay）葡萄； 尽管在法国之外也有所种植，但是天性娇贵刁钻的黑皮诺却只在勃艮第的风土上才能展现独一无二的优雅风姿；而被广泛在世界各地种植的霞多丽，也惟有在勃艮第才展现出特别的魅力，不仅拥有丰满的口感，还保有少见的强劲与细腻变化，以及惊人的久存潜力。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">当然，勃艮第还有为数不少的其他优秀葡萄品种，如佳美Gamay，阿里高特AIigote，但总的来说，当区最具代表性及最优秀的葡萄品种还当属贝露娃和雪当利。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">每种葡萄适合的土壤：黑皮诺适合在排水性顺畅的石灰质灰石土壤中生长。 根据小片葡萄园所处土壤的石灰含量和朝向不同， 酿出的葡萄特点也不尽相同：或清新优雅，或强劲有力。霞多丽适合在含较多粘土的石灰质泥灰土壤中生长，优雅贺敬之的想起在这种土壤中展现。所含粘土的比例决定了勃艮第干白葡萄酒的香气张力多少&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">三种产酒单位：&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">独立酒庄、酒商与酿酒合作社是勃艮第三种主要生产葡萄酒的单位。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">独立酒庄只产自家葡萄园所产的葡萄酒，比较容易保有葡萄园的特殊风味以及庄主的个人风格&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">酒商除了生产来自自有葡萄园的葡萄酒，也采买葡萄酿造，或买酿好的葡萄酒，经过培养之后以酒商的名义装瓶销售，不同于波尔多酒商装瓶的酒常常品级较低，勃艮第许多知名的酒商也能生产出顶级的上好勃艮第葡萄酒。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">至于无法自行酿酒的葡萄农，则将采收的葡萄交给加盟的酿酒合作社酒厂，统一酿造与销售，产量大的酿酒合作社则提供最价廉物美的勃艮第葡萄酒。在勃艮第，这三者之间的关系特别复杂，也深深影响葡萄酒的特色。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">4级葡萄园：&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">勃艮第所有的葡萄园全都依据园中的自然条件，详细分成四个等级，虽然产区很多，但四个等级却很容易辩识，每一瓶勃艮第葡萄酒的标签上都会注明。 品级最高的称为特级葡萄园(Grand Cru)，仅有不到2％的葡萄园被列入这个等级，大多是条件最好的村庄的最精华区。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">著名产酒村里最好的或仅次于特级的葡萄园被列为一级葡萄园(1er Cru)，有近11％的葡萄园属于这个等级。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">在勃艮第，惟有条件好的著名产酒村才能以村名作为葡萄酒的名字，这些村子里的优质葡萄园，除了特级与一级外，即属于村庄级AOC(appellation c法定产区。 其他符合种植条件的葡萄园则是一般的勃艮第地方性AOC法定产区，也就是第四级葡萄园。&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">5大产区：&nbsp;</span><br style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;" />
<span style="color: rgb(17, 17, 17); font-family: Helvetica, Arial, sans-serif; line-height: 19.440000534057617px;">勃艮第两万四千多公顷的葡萄园每年生产一亿三千多公升的葡萄酒，他们分别来自五个不同的产区，由南到北相差两百多公里。这些产区因为自然与人文环境的不同，即使同样使用贝露娃或雪当利葡萄酿酒，却各有不同的风味．最北边的夏布利(Chablis)因为气候寒冷，只产白酒，霞多丽白酒出现酸度高、口感较为清淡的特色，并带有特殊的矿石香气。 往南位于第戎市(Dijon)南边的尼依区(C&ocirc;te de Nuits，也称夜丘区)则完全是红酒的天下，出产全世界最顶级的黑皮诺红酒，细腻而强劲，而且很耐久存。博讷区(C&ocirc;te de Beaune，也称宝望丘区)紧邻夜丘区南边，除了出产更为柔美可口的黑皮诺红酒，这里也出产全球最顶尖的雪当利白酒。再往南是夏隆内区(C&ocirc;te Chalonnaise)， 同时出产红、白酒。最南边则是马孔区(Maconnais)，黑皮诺红酒已难有好表现，主要出产成熟度高的霞多丽白酒，口感较为圆润，常带有甜美的热带水果香气。</span></p>                           <div style="padding:8px; margin-top:15px; text-align:left; border-top:1px solid #ccc;">
         
